# Validating user input in programming

# The below program will check the age that entered by the user is a valid
# or invalid number

age = int(input("Enter the age: ")) # User input

if age <=110 and age >=1: # if Condition
    print("The age that you have entered is a valid number") # Code to run if the condition is true
else:
    print("Invalid age") # Code to run if the condition is false

