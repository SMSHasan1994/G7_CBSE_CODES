# Mathematical Operators in Programming

# Create two variables called num1 and num2

num1 = 15
num2 = 3

#Addition operator
add = num1+num2
print("The sum is: ", add)
print()

#Subtraction operator
sub = num1-num2
print("The difference is: ", sub)
print()

#Multiplication operator
mul = num1*num2
print("The product is: ", mul)
print()

#Division operator
div = num1/2
print("The quotient is: ", div)
